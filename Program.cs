
//ввод массива, вывод по строчкам, нахождение максимального элемента

using System

public class MainClass
{
    public static void Main()
    {
        int a, b;
        a = Convert.ToInt32(Console.ReadLine());
        b = Convert.ToInt32(Console.ReadLine());
        for (int i = a;  i <= b; i++)
        {
            Console.Write(Convert.To.Char(i));
        }



    }

}
